const mdHeaderRegex = {
  '######': /^(|<p>|( {4}){0,}(<li>|<ol>)) {0,}#{6} (.+)/gm,
  '#####': /^(|<p>|( {4}){0,}(<li>|<ol>)) {0,}#{5} (.+)/gm,
  '####': /^(|<p>|( {4}){0,}(<li>|<ol>)) {0,}#{4} (.+)/gm,
  '###': /^(|<p>|( {4}){0,}(<li>|<ol>)) {0,}#{3} (.+)/gm,
  '##': /^(|<p>|( {4}){0,}(<li>|<ol>)) {0,}#{2} (.+)/gm,
  '#': /^(|( {4}){0,}(<li>|<ol>|<blockquote>)) {0,}# (.+)/gm,
};

export default mdHeaderRegex;
