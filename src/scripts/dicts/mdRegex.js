// DICCIONARIO MARKDOWN TO REGEX
const mdRegex = {
  '***': /\*\*\*(.*?)\*\*\*/g,
  '___': /\_\_\_(.*?)\_\_\_/g,
  '__*': /\_\_\*(.*?)\*\_\_/g,
  '**_': /\*\*\_(.*?)\_\*\*/g,

  '**': /\*\*(.*?)\*\*/g,
  '__': /\_\_(.*?)\_\_/g,

  '*': /\*(.*?)\*/g,
  '_': /\_(.*?)\_/g,
  
  '``': /\`\`(.*?)\`\`/g,
  '`': /\`(.*?)\`/g,
  '    ': /^ {4,}(.+)/gm,
  '\t': /^\t(.+)/gm,

  '  \n': /()\s+\s+$/gm,

  '######': /^#{6} (.+)/gm,
  '#####': /^#{5} (.+)/gm,
  '####': /^#{4} (.+)/gm,
  '###': /^#{3} (.+)/gm,
  '##': /^#{2} (.+)/gm,
  '#': /^# (.+)/gm,

  '==': /(.+?)\n+={2,}/g,
  '--': /(.+?)\n+-{2,}/g,

  '>': /^>{1,}(.+)/gm,
};

export default mdRegex;
