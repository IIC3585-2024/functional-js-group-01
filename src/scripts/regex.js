// DICCIONARIO MARKDOWN TO REGEX
const mdToRegex = {
  '#': /^#(.+)/gm,
  '##': /^#{2}(.+)/gm,
  '###': /^#{3}(.+)/gm,
  '####': /^#{4}(.+)/gm,
  '#####': /^#{5}(.+)/gm,
  '######': /^#{6}(.+)/gm,

  '*': /\*(.*?)\*/g,
  '_': /\_(.*?)\_/g,

  '**': /\*\*(.*?)\*\*/g,
  '__': /\_\_(.*?)\_\_/g,

  '***': /\*\*\*(.*?)\*\*\*/g,
  '___': /\_\_\_(.*?)\_\_\_/g,
  '__*': /\_\_\*(.*?)\*\_\_/g,
  '**_': /\*\*\_(.*?)\_\*\*/g,

  '``': /\`\`(.*?)\`\`/g,
  '`': /\`(.*?)\`/g,

  '  \n': /()\s+\s+$/gm,

  '>': /^>(.+)/gm,
};

export default mdToRegex;
